package oa.common.model;

public class User {

    String userId;
    String userName;
    String userDepId;
    String userDepName;

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserDepId() {
        return userDepId;
    }

    public void setUserDepId(String userDepId) {
        this.userDepId = userDepId;
    }

    public String getUserDepName() {
        return userDepName;
    }

    public void setUserDepName(String userDepName) {
        this.userDepName = userDepName;
    }
}
