package oa.q311.bin.model;

public class Q311 {

    String uuid;
    String reqEmpId;
    String reqDepNo;
    String reqDate;

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getReqEmpId() {
        return reqEmpId;
    }

    public void setReqEmpId(String reqEmpId) {
        this.reqEmpId = reqEmpId;
    }

    public String getReqDepNo() {
        return reqDepNo;
    }

    public void setReqDepNo(String reqDepNo) {
        this.reqDepNo = reqDepNo;
    }

    public String getReqDate() {
        return reqDate;
    }

    public void setReqDate(String reqDate) {
        this.reqDate = reqDate;
    }
}
